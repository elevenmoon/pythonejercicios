#Iteradores corren un determinado numero de veces una pieza de codigo

meses = ['octubre','febrero','marzo']

#ciclo for
for mes in meses:
    print(mes)

print(f'estoy viajando por el mundo en el mes de {mes}')

#imprimir numeros
for numero in range(0,20,5):
    print(numero)

materias = ['castellano','matematicas','fisica','quimica']

for materia in materias:
    print(materia)

for dedosendos in range(0,100,2):
    print(dedosendos)
